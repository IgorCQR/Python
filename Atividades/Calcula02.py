nume00 = input("Informe um número: ")
nume01 = input("Informe outro número: ")

result = int(nume00) + int(nume01)
result01 = int(nume00) - int(nume01)
result02 = int(nume00) * int(nume01)
result03 = int(nume00) / int(nume01)

print(f"A soma entre {nume00} e {nume01} é {result}")
print(f"A diferença entre {nume00} e {nume01} é {result01}")
print(f"O produto entre {nume00} e {nume01} é {result02}")
print(f"A divisão entre {nume00} e {nume01} é {result03}")
