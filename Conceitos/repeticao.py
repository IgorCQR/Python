contador = int(input('Informe um numero: '))
while contador >= 0: # faz uma ação enquanto determinada condição for verdadeira
    print(f"Numero: {contador}")
    contador -= 1 # diminui um ao contador

print('OUTRA REPETICAO')

for i in range(1,10): # faz uso de uma sequencia
    print(f"Numero: {i}")