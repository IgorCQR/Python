x = 3.14
z = -5
y = 7
numero = int(input('Informe um número: '))

result = round(x) # ROUND arredonda o numero
result1 = abs(z) # ABS espelha o numero
result2 = pow(5, 0) # POW é equivalente a potencia (base ; expoente)
result3 = max(x,y,z) # MAX compara as variaveis e pega o MAIOR
result4 = min(x,y,z) # o oposto do MAX
result5 = 7//2 # Divisão INTEIRA
result6 = numero**(1/2)

print(result)
print(result1)
print(result2)
print(result3)
print(result4)
print(result5)
print(result6)