#CALCULAR AREA DO TRIANGULO
print("Vamos calcular a area de um triangulo!")

base = float(input("Diga qual o valor da base: "))
altura = float(input("Diga qual a altura desse triangulo: "))

area = (base * altura) / 2

print(f"A area desse triangulo é {area}cm")
